package MainMethod1;

public class Switchstatement2 {
	
	public static void main (String [] args) {
		
		char ch = 'a';
		
		switch (ch) {
		case 'S':
			System.out.println("match with s");
			break;
			
		case 'd':
			System.out.println("match with d");
			break;
		case 'a':
			System.out.println("match with a");
			break;
			
			default :
				System.out.println("match is not available");
		}
		
	}

}
