package loopstatement2;

public class forloop1 {
	public static void main(String [] args) {
		// to print 1 to 10 number using for loop
		
		// Starting point-1
		//ending point-10
		
		
		for(int name=1;name<=10;name++) {
			System.out.println(name);
		}
		System.out.println("loop completed");
		
			
		
	}

}
