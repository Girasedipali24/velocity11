package operators;

public class incdecoperator {
	public static void main(String [] args) {
		int a=10;
		int b=20;
		int c;
		
		c=a; // assign operator
		System.out.println(c);
		
		a++; // increment by one
		System.out.println(c);
		
		c=c+1; // increment by one
		System.out.println(c);
		
		c++;
		System.out.println(c);
		
		c++;
		System.out.println(c);
		
		a++;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		
		b++;
		System.out.println(b);
		
		b++;
		System.out.println(b);
		
		b++;
		System.out.println(b);
		
		
	}

}
